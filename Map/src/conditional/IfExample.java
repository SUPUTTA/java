package conditional;

public class IfExample {

    public static void main(String[] args) {
        int x = 10;
        int y = 20;

        if(x+y < 10) {
            System.out.println("x+y is the less than 10");
        }
        else {
            System.out.println("x+y is the greater than 20");
        }
    }
}
